import 'package:flutter/material.dart';

TextStyle gettextstyle(Color color){

  return TextStyle(
    fontSize: 20.0,fontWeight: FontWeight.w700,color: color
  );
}

BoxShadow getboxshadow(Color color){

  return BoxShadow(offset: Offset(0, 5.0),
  
  blurRadius: 5.0,
  color: color
  );
}



                     
 

  



  
